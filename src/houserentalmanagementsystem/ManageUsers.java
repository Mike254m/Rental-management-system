    private void retrieveusers() {
        try {
            String sql = "SELECT user_id, username, PhoneNumber, password, role, SecurityQuestion, Answer FROM Users";
            con = DriverManager.getConnection("jdbc:mysql://localhost/rentalmanagementsystem", "root", "");
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) userstable.getModel();
            model.setRowCount(0);
            while (rs.next()) {
                String id = rs.getString(1);
                String username = rs.getString(2);
                String phonenumber = rs.getString(3);
                String pass = rs.getString(4);
                String position = rs.getString(5);
                String quest = rs.getString(6);
                String answers = rs.getString(7);
                model.addRow(new String[]{id, username, phonenumber, pass, position, quest, answers});
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public Boolean updateusers(String id, String name, String phns, String passt, String usrole, String userquery, String ansquest) {
        String query = "UPDATE Users SET username='" + name + "',phonenumber='" + phns + "',password='" + passt + "', role ='" + usrole + "', SecurityQuestion='" + userquery + "',Answer='" + ansquest + "' WHERE user_id= '" + id + "'";
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/rentalmanagementsystem", "root", "");
            ps = con.prepareStatement(query);
            ps.execute(query);
            return true;
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
            return false;
        }
    }

    public Boolean deleteusers(String id) {

        String sql = "DELETE FROM Users WHERE user_id ='" + id + "'";
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/rentalmanagementsystem", "root", "");
            ps = con.prepareStatement(sql);
            ps.execute(sql);
            return true;
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
            return false;
        }
    }

    private void filterusers(String query) {
        DefaultTableModel model2 =  (DefaultTableModel) userstable.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(model2);
        userstable.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(query));
    }

