    private void updbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updbtnActionPerformed
        try {
            if (ptxt.getText().isEmpty() || usertxt.getText().isEmpty() || passtxt.getText().isEmpty()|| roleu.getText().isEmpty()|| secu.getText().isEmpty()|| ansu.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "No row is selected..!!\nPlease select a row to update...");
            } else {
                int index = userstable.getSelectedRow();
                String id = userstable.getValueAt(index, 0).toString();  //index=rownumber and 0 represents first column..

                if (updateusers(id, ptxt.getText(), usertxt.getText(), passtxt.getText(), roleu.getText(), secu.getText(),ansu.getText())){
                    JOptionPane.showMessageDialog(null, "Data Updated Successfully");
                    ptxt.setText("");
                    usertxt.setText("");
                    passtxt.setText("");
                    roleu.setText("");
                    secu.setText("");
                    ansu.setText("");
                    retrieveusers();
                } else {
                    JOptionPane.showMessageDialog(null, "Data Not Updated ");
                }
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, e);
        }  
    }//GEN-LAST:event_updbtnActionPerformed

    private void delbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delbtnActionPerformed

        try {
            String[] options = {"Yes", "No"};
            int answer = JOptionPane.showOptionDialog(null, " Are you Sure to delete ??", "Delete Options", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[1]);

            if (answer == 0) {
              String id = userstable.getValueAt(userstable.getSelectedRow(), 0).toString();
                if (deleteusers(id)) {
                    JOptionPane.showMessageDialog(null, "Data Deleted Successfully");
                    ptxt.setText("");
                    usertxt.setText("");
                    passtxt.setText("");
                    roleu.setText("");
                    secu.setText("");
                    ansu.setText("");
                    retrieveusers();
                } else {
                    JOptionPane.showMessageDialog(null, " Data Not Deleted ");
                }
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, e);
        }  
    }//GEN-LAST:event_delbtnActionPerformed

